#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	str = (unsigned char *)s;
	i = 0;
	while (str[i] && i < n && str[i] != (unsigned char)c)
		i++;
	if (i < n && str[i] == (unsigned char)c)
		return ((void *)s + i);
	return (NULL);
}
