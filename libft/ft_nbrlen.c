#include "libft.h"

int	ft_nbrlen(long long int n)
{
	int	len;

	if (n < 0)
	{
		len = 2;
		n = -n;
	}
	else
		len = 1;
	while (n > 9)
	{
		len++;
		n = n / 10;
	}
	return (len);
}
